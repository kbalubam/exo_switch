let jour = prompt("Quel jour de la semaine sommes-nous? ")

switch (jour) {
    case "Lundi":
        alert("Nous sommes lundi")
        break;
    case "Mardi":
        alert("Nous sommes Mardi ")
        break;
    case "Mercredi":
        alert("Nous sommes Mercredi")
        break;
    case "Jeudi":
        alert("Nous sommes Jeudi")
        break;
    case "Vendredi":
        alert("Nous sommes Vendredi")
        break;
    case "Samedi":
        alert("Nous sommes Samedi")
        break;
    case "Dimanche":
        alert("Nous sommes Dimanche")
        break;
    default:
        break;
}